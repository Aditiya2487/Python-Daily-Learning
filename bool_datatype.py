a=True
b=False
print("The value of a is:",a)
print("The type of a is:",type(a))  # Output: <class 'bool'>
print("The value of b is:",b)
print("The type of b is:",type(b))  # Output: <class 'bool'>
print("a AND b is:",a and b)  # Output: False
print("a OR b is:",a or b)   # Output: True
print("NOT a is:",not a)     # Output: False
print("NOT b is:",not b)     # Output: True
print("a equals b:",a == b)  # Output: False
print("a not equals b:",a != b)  # Output: True
print("a greater than b:",a > b)  # Output: True
print("a less than b:",a < b)     # Output: False
print("a greater than or equal to b:",a >= b)  # Output: True
print("a less than or equal to b:",a <= b)     # Output: False
# Boolean values in arithmetic operations
print("a + 5 =",a + 5)  # Output: 6
print("b + 5 =",b + 5)  # Output: 5
print("a * 10 =",a * 10)  # Output: 10